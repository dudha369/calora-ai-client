/**
 * Приводит результат t(key, { returnObjects: true }) к обычному словарю
 * "строка → строка". Форму вложенного JSON-объекта i18next на этапе
 * компиляции не выводит (возвращает string | object | ... в зависимости
 * от перегрузки), а мы точно знаем, что в наших локалях под такими ключами
 * (allergens, nova_groups) всегда лежит плоский { ключ: строка }.
 *
 * Специально не завязываемся на тип самой функции t() (TFunction<...>) —
 * он завязан на конкретную минорную версию i18next и легко ломается при
 * апдейте пакета. Здесь работаем только с уже полученным значением,
 * поэтому сигнатура тривиальная и стабильная.
 */
export function asStringDict(value: unknown): Record<string, string> {
  return value as Record<string, string>;
}
